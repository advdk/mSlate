ipcMain.handle('claude-ask-notes', async (_event, question: string): Promise<ClaudeNotesAnswer> => {
  const normalizedQuestion = question.trim();
  if (!normalizedQuestion) {
    throw new Error('Enter a question before asking Claude about your notes.');
  }

  const noteContext = getClaudeNoteContext(normalizedQuestion);
  if (!noteContext.context) {
    return {
      answer: 'I could not find relevant note context for that question.',
      sources: [],
    };
  }

  const answer = await callClaude(
    'You answer questions about a user\'s personal markdown notes. Use only the supplied note context. If the context is insufficient, say that clearly. Be concise and practical. Mention source filenames in the answer when relevant.',
    `Question: ${normalizedQuestion}\n\nNOTE CONTEXT:\n${noteContext.context}`,
    1200,
  );

  return {
    answer,
    sources: noteContext.sources,
  };
});

