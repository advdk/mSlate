ipcMain.handle('delete-note', async (_event, filename: string): Promise<void> => {
  const normalizedFilename = normalizeNotePath(filename);
  if (!isNotePath(normalizedFilename)) throw new Error('Invalid note filename');

  const noteSummary = getNoteSummary(normalizedFilename);
  if (noteSummary.kind !== 'note') {
    throw new Error('Journal entries cannot be deleted.');
  }

  const filePath = getAbsoluteNotePath(normalizedFilename);
  if (fs.existsSync(filePath)) {
    fs.unlinkSync(filePath);
    removeEmptyParentDirectories(filePath);
  }

  removeIndexedNote(normalizedFilename);

  if (!settings.pinnedNotes) {
    settings.pinnedNotes = [];
  }
  const nextPinnedNotes = settings.pinnedNotes.filter((entry) => entry !== normalizedFilename);
  if (nextPinnedNotes.length !== settings.pinnedNotes.length) {
    settings.pinnedNotes = nextPinnedNotes;
    saveSettings(settings);
  }

  mainWindow?.webContents.send('notes-changed');
});

ipcMain.handle('read-note', async (_event, filename: string) => {
  const normalizedFilename = normalizeNotePath(filename);
  if (!isNotePath(normalizedFilename)) throw new Error('Invalid note filename');
  ensureNotesFolder();
  const filePath = getAbsoluteNotePath(normalizedFilename);
  if (fs.existsSync(filePath)) {
    return fs.readFileSync(filePath, 'utf-8');
  }
  const content = getNewNoteContent(normalizedFilename);
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, content, 'utf-8');
  upsertIndexedNote(normalizedFilename, content);
  return content;
});

ipcMain.handle('write-note', async (_event, filename: string, content: string) => {
  const normalizedFilename = normalizeNotePath(filename);
  if (!isNotePath(normalizedFilename)) throw new Error('Invalid note filename');
  ensureNotesFolder();
  const filePath = getAbsoluteNotePath(normalizedFilename);
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  const tmpPath = filePath + '.tmp';
  // Atomic write: write to temp file, then rename
  fs.writeFileSync(tmpPath, content, 'utf-8');
  fs.renameSync(tmpPath, filePath);
  upsertIndexedNote(normalizedFilename, content);
});

ipcMain.handle('save-pasted-image', async (_event, filename: string, mimeType: string, bytes: number[]) => {
  const normalizedFilename = normalizeNotePath(filename);
  if (!isNotePath(normalizedFilename)) throw new Error('Invalid note filename');
  if (!isSupportedImageMimeType(mimeType)) throw new Error('Unsupported image format');
  if (!Array.isArray(bytes) || bytes.length === 0) throw new Error('Image data is required');

  const attachmentsFolder = ensureAttachmentsFolder();
  const extension = getImageExtension(mimeType);
  const attachmentFilename = createAttachmentFilename(normalizedFilename, extension);
  const attachmentPath = path.join(attachmentsFolder, attachmentFilename);
  fs.writeFileSync(attachmentPath, Buffer.from(bytes));

  const noteFolder = getNoteFolder(normalizedFilename);
  const relativeAssetPath = path.relative(
    noteFolder ? path.join(settings.notesFolder, ...noteFolder.split('/')) : settings.notesFolder,
    attachmentPath,
  ).replace(/\\/g, '/');

  return relativeAssetPath;
});

ipcMain.handle('resolve-note-asset-url', async (_event, filename: string, assetPath: string) => {
  const normalizedFilename = normalizeNotePath(filename);
  if (!isNotePath(normalizedFilename)) throw new Error('Invalid note filename');

  const normalizedAssetPath = normalizeAssetPath(assetPath);
  if (!normalizedAssetPath || /^(https?:|data:|blob:|file:)/i.test(normalizedAssetPath)) {
    return normalizedAssetPath || null;
  }

  ensureNotesFolder();
  const notesRoot = path.resolve(settings.notesFolder);
  const noteDirectory = path.dirname(getAbsoluteNotePath(normalizedFilename));
  const resolvedPath = path.resolve(noteDirectory, normalizedAssetPath);
  if (!(resolvedPath === notesRoot || resolvedPath.startsWith(`${notesRoot}${path.sep}`))) {
    return null;
  }

  if (!fs.existsSync(resolvedPath)) {
    return null;
  }

  const mimeType = getImageMimeTypeFromPath(resolvedPath);
  const fileBuffer = fs.readFileSync(resolvedPath);
  return `data:${mimeType};base64,${fileBuffer.toString('base64')}`;
});

