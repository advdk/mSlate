function postJson<T>(urlString: string, headers: Record<string, string>, body: unknown): Promise<T> {
  return new Promise((resolve, reject) => {
    const url = new URL(urlString);
    const payload = JSON.stringify(body);
    const request = https.request(
      {
        protocol: url.protocol,
        hostname: url.hostname,
        port: url.port,
        path: `${url.pathname}${url.search}`,
        method: 'POST',
        headers: {
          'Content-Type': 'application/json',
          'Content-Length': Buffer.byteLength(payload).toString(),
          ...headers,
        },
      },
      (response) => {
        const chunks: Buffer[] = [];
        response.on('data', (chunk) => {
          chunks.push(Buffer.isBuffer(chunk) ? chunk : Buffer.from(chunk));
        });
        response.on('end', () => {
          const raw = Buffer.concat(chunks).toString('utf-8');
          const parsed = raw ? JSON.parse(raw) as { error?: { message?: string } } & T : {} as T;
          if ((response.statusCode ?? 500) >= 400) {
            reject(new Error(parsed.error?.message || `Anthropic request failed with status ${response.statusCode ?? 500}`));
            return;
          }

          resolve(parsed as T);
        });
      },
    );

    request.on('error', reject);
    request.write(payload);
    request.end();
  });
}

function extractClaudeText(response: { content?: Array<{ type: string; text?: string }> }): string {
  const text = (response.content ?? [])
    .filter((block) => block.type === 'text' && typeof block.text === 'string')
    .map((block) => block.text?.trim() ?? '')
    .filter((block) => block.length > 0)
    .join('\n\n');

  if (!text) {
    throw new Error('Claude returned an empty response.');
  }

  return text;
}

async function callClaude(system: string, prompt: string, maxTokens: number): Promise<string> {
  const claudeSettings = requireClaudeSettings();
  const response = await postJson<{ content?: Array<{ type: string; text?: string }> }>(
    'https://api.anthropic.com/v1/messages',
    {
      'x-api-key': claudeSettings.apiKey,
      'anthropic-version': '2023-06-01',
    },
    {
      model: claudeSettings.model,
      max_tokens: maxTokens,
      system,
      messages: [
        {
          role: 'user',
          content: prompt,
        },
      ],
    },
  );

  return extractClaudeText(response);
}

function getClaudeNoteContext(question: string): { context: string; sources: string[] } {
  const results = searchNotesWithFallback(question).slice(0, 8);
  const uniqueFiles = Array.from(new Set(results.map((result) => result.note.filename))).slice(0, 4);
  const sources: string[] = [];
  const sections: string[] = [];

  for (const filename of uniqueFiles) {
    try {
      const content = fs.readFileSync(getAbsoluteNotePath(filename), 'utf-8').trim();
      const snippet = results.find((result) => result.note.filename === filename)?.snippet ?? '';
      const excerpt = content.length > 5000 ? `${content.slice(0, 5000).trim()}\n...` : content;
      sections.push([
        `FILE: ${filename}`,
        snippet ? `SEARCH SNIPPET: ${snippet}` : '',
        'CONTENT:',
        excerpt,
      ].filter(Boolean).join('\n'));
      sources.push(filename);
    } catch (error) {
      console.error(`Failed to load note context for ${filename}:`, error);
    }
  }

  return {
    context: sections.join('\n\n---\n\n'),
    sources,
  };
}

function createImportSessionId(): string {
  return `import-${Date.now()}-${Math.random().toString(36).slice(2, 10)}`;
}

function scanMarkdownImportFolder(folderPath: string): MarkdownImportScanResult {
  const validFiles: MarkdownImportScanResult['validFiles'] = [];
  const invalidFiles: MarkdownImportInvalidFile[] = [];
  const candidates = new Map<string, { filename: string; sourcePath: string; conflict: boolean }>();

  const entries = fs.readdirSync(folderPath, { withFileTypes: true });
  for (const entry of entries) {
    if (!entry.isFile()) continue;

    const extension = path.extname(entry.name).toLowerCase();
    if (extension !== '.md') continue;

    const sourcePath = path.join(folderPath, entry.name);
    if (!isJournalFilename(entry.name)) {
      invalidFiles.push({ filename: entry.name, reason: 'Filename must match YYYY-MM-DD.md' });
      continue;
    }

    const id = createImportSessionId();
    const conflict = fs.existsSync(path.join(settings.notesFolder, entry.name));
    const candidate = { filename: entry.name, sourcePath, conflict };
    candidates.set(id, candidate);
    validFiles.push({ id, filename: entry.name, conflict });
  }

  const sessionId = createImportSessionId();
  importSessions.set(sessionId, { invalidFiles, candidates });

  return {
    sessionId,
    sourceFolder: folderPath,
    validFiles: validFiles.sort((a, b) => b.filename.localeCompare(a.filename)),
    invalidFiles: invalidFiles.sort((a, b) => a.filename.localeCompare(b.filename)),
  };
}

function applyMarkdownImport(sessionId: string, decisions: MarkdownImportDecision[]): MarkdownImportResult {
  const session = importSessions.get(sessionId);
  if (!session) {
    throw new Error('Import session expired');
  }

  const result: MarkdownImportResult = {
    imported: [],
    overwritten: [],
    skipped: [],
    invalid: session.invalidFiles,
    failed: [],
  };

  const decisionsById = new Map(decisions.map((decision) => [decision.id, decision.action]));

  for (const [id, candidate] of session.candidates.entries()) {
    const action = decisionsById.get(id) ?? (candidate.conflict ? 'skip' : 'import');
    if (action === 'skip') {
      result.skipped.push(candidate.filename);
      continue;
    }

    if (candidate.conflict && action !== 'overwrite') {
      result.skipped.push(candidate.filename);
      continue;
    }

    try {
      const content = fs.readFileSync(candidate.sourcePath, 'utf-8');
      const destinationPath = path.join(settings.notesFolder, candidate.filename);
      fs.writeFileSync(destinationPath, content, 'utf-8');
      upsertIndexedNote(candidate.filename, content);

      if (candidate.conflict) {
        result.overwritten.push(candidate.filename);
      } else {
        result.imported.push(candidate.filename);
      }
    } catch (error) {
      result.failed.push({
        filename: candidate.filename,
        reason: error instanceof Error ? error.message : 'Import failed',
      });
    }
  }

  importSessions.delete(sessionId);
  mainWindow?.webContents.send('notes-changed');
  return result;
}

