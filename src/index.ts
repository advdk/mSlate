function broadcastWindowState(): void {
  if (!mainWindow) return;

  mainWindow.webContents.send('window-state-changed', {
    isDesktop: true,
    isMaximized: mainWindow.isMaximized(),
  });
}

const createWindow = (): void => {
  initializeSearchIndex();
  if (process.platform === 'darwin') {
    buildApplicationMenu();
  } else {
    Menu.setApplicationMenu(null);
  }

  mainWindow = new BrowserWindow({
    height: 700,
    width: 1100,
    minWidth: 600,
    minHeight: 400,
    backgroundColor: '#1a1a2e',
    title: `mSlate — ${getTodayFilename().replace('.md', '')}`,
    frame: process.platform === 'darwin',
    titleBarStyle: process.platform === 'darwin' ? 'hiddenInset' : 'hidden',
    autoHideMenuBar: process.platform !== 'darwin',
    webPreferences: {
      preload: MAIN_WINDOW_PRELOAD_WEBPACK_ENTRY,
      contextIsolation: true,
      nodeIntegration: false,
    },
  });

  mainWindow.setMenuBarVisibility(process.platform === 'darwin');

  mainWindow.loadURL(MAIN_WINDOW_WEBPACK_ENTRY);

  startWatchingNotes();

  mainWindow.on('maximize', broadcastWindowState);
  mainWindow.on('unmaximize', broadcastWindowState);

  mainWindow.on('closed', () => {
    stopWatchingNotes();
    mainWindow = null;
  });

  // Update title when note changes
  ipcMain.on('set-title', (_event, noteTitle: string) => {
    if (mainWindow) mainWindow.setTitle(`mSlate \u2014 ${noteTitle}`);
  });
};

app.on('ready', createWindow);

