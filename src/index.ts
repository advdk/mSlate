// --- Helpers ---

function isSupportedImageMimeType(mimeType: string): boolean {
  return ['image/png', 'image/jpeg', 'image/gif', 'image/webp'].includes(mimeType);
}

function getImageExtension(mimeType: string): string {
  switch (mimeType) {
    case 'image/png':
      return 'png';
    case 'image/jpeg':
      return 'jpg';
    case 'image/gif':
      return 'gif';
    case 'image/webp':
      return 'webp';
    default:
      throw new Error('Unsupported image format');
  }
}

function getImageMimeTypeFromPath(filePath: string): string {
  switch (path.extname(filePath).toLowerCase()) {
    case '.png':
      return 'image/png';
    case '.jpg':
    case '.jpeg':
      return 'image/jpeg';
    case '.gif':
      return 'image/gif';
    case '.webp':
      return 'image/webp';
    default:
      return 'application/octet-stream';
  }
}

function ensureAttachmentsFolder(): string {
  ensureNotesFolder();
  const attachmentsFolder = path.join(settings.notesFolder, 'attachments');
  if (!fs.existsSync(attachmentsFolder)) {
    fs.mkdirSync(attachmentsFolder, { recursive: true });
  }
  return attachmentsFolder;
}

function createAttachmentFilename(noteFilename: string, extension: string): string {
  const basename = noteFilename.replace(/\.md$/i, '').replace(/[^a-zA-Z0-9_-]/g, '-');
  return `${basename}-${Date.now()}.${extension}`;
}

function normalizeAssetPath(assetPath: string): string {
  if (!assetPath || /^(https?:|data:|blob:|file:)/i.test(assetPath)) {
    return assetPath;
  }

  const normalized = path.normalize(assetPath).replace(/^([/\\])+/, '');
  if (normalized.startsWith('..') || path.isAbsolute(normalized)) {
    throw new Error('Invalid asset path');
  }

  return normalized;
}

function applyTemplate(template: string): string {
  const now = new Date();
  const yyyy = now.getFullYear();
  const mm = String(now.getMonth() + 1).padStart(2, '0');
  const dd = String(now.getDate()).padStart(2, '0');
  const dateStr = `${yyyy}-${mm}-${dd}`;
  const dayOfWeek = now.toLocaleDateString('en-US', { weekday: 'long' });
  return template
    .replace(/\{\{date\}\}/g, dateStr)
    .replace(/\{\{day_of_week\}\}/g, dayOfWeek);
}

function getNewNoteContent(filename: string): string {
  if (isJournalFilename(path.posix.basename(filename)) && settings.noteTemplate) {
    return applyTemplate(settings.noteTemplate);
  }

  return '';
}

function getAbsoluteNotePath(notePath: string): string {
  const normalizedPath = normalizeNotePath(notePath);
  const absolutePath = path.resolve(settings.notesFolder, ...normalizedPath.split('/'));
  const notesRoot = path.resolve(settings.notesFolder);
  if (!(absolutePath === notesRoot || absolutePath.startsWith(`${notesRoot}${path.sep}`))) {
    throw new Error('Invalid note path');
  }

  return absolutePath;
}

function collectNotePaths(directoryPath = settings.notesFolder, relativeDirectory = ''): string[] {
  const results: string[] = [];
  const entries = fs.readdirSync(directoryPath, { withFileTypes: true });

  for (const entry of entries) {
    if (entry.name === 'attachments' && !relativeDirectory) {
      continue;
    }

    const relativePath = relativeDirectory ? `${relativeDirectory}/${entry.name}` : entry.name;
    const absolutePath = path.join(directoryPath, entry.name);

    if (entry.isDirectory()) {
      results.push(...collectNotePaths(absolutePath, relativePath.replace(/\\/g, '/')));
      continue;
    }

    const normalizedPath = relativePath.replace(/\\/g, '/');
    if (isNotePath(normalizedPath)) {
      results.push(normalizedPath);
    }
  }

  return results;
}

function removeEmptyParentDirectories(startPath: string): void {
  const notesRoot = path.resolve(settings.notesFolder);
  let currentPath = path.dirname(startPath);

  while (currentPath.startsWith(notesRoot) && currentPath !== notesRoot) {
    const entries = fs.readdirSync(currentPath);
    if (entries.length > 0) {
      return;
    }

    fs.rmdirSync(currentPath);
    currentPath = path.dirname(currentPath);
  }
}

function getIndexedNotes(): IndexedNoteRecord[] {
  ensureNotesFolder();
  return collectNotePaths()
    .map((filename) => ({
      filename,
      content: fs.readFileSync(getAbsoluteNotePath(filename), 'utf-8'),
    }));
}

function searchNotesLinear(query: string): NoteSearchResult[] {
  ensureNotesFolder();
  if (!query || query.trim().length === 0) return [];

  const searchTerm = query.toLowerCase();
  const files = collectNotePaths();
  const results: NoteSearchResult[] = [];

  for (const file of files) {
    const content = fs.readFileSync(getAbsoluteNotePath(file), 'utf-8');
    const lowerContent = content.toLowerCase();
    const idx = lowerContent.indexOf(searchTerm);
    if (idx !== -1) {
      const start = Math.max(0, idx - 30);
      const end = Math.min(content.length, idx + query.length + 50);
      const snippet = (start > 0 ? '...' : '') + content.substring(start, end).trim() + (end < content.length ? '...' : '');
      results.push({ note: getNoteSummary(file), snippet });
    }
  }

  return results.sort((a, b) => compareNoteFilenames(a.note.filename, b.note.filename));
}

function searchNotesWithFallback(query: string): NoteSearchResult[] {
  if (!query || query.trim().length === 0) {
    return [];
  }

  if (canUseSearchIndex()) {
    try {
      return searchIndex.searchNotes(query).map((result) => ({
        note: getNoteSummary(result.filename),
        snippet: result.snippet,
      }));
    } catch (error) {
      disableSearchIndex(error, 'search notes');
      console.error('Indexed search failed, falling back to linear search:', error);
    }
  }

  try {
    return searchNotesLinear(query);
  } catch (error) {
    console.error('Linear search failed:', error);
    return [];
  }
}

function getSearchIndexFallbackStatus(): SearchIndexStatus {
  return {
    ready: false,
    databasePath: searchIndexPath,
    notesFolder: settings.notesFolder,
    noteCount: 0,
    lastIndexedAt: null,
  };
}

function getSearchIndexStatusSafe(): SearchIndexStatus {
  try {
    const status = searchIndex.getStatus();
    if (searchIndexDisabledReason) {
      return { ...status, ready: false };
    }
    return status;
  } catch {
    return getSearchIndexFallbackStatus();
  }
}

function disableSearchIndex(error: unknown, action: string): void {
  const message = error instanceof Error ? error.message : String(error);
  if (!searchIndexDisabledReason) {
    console.error(`Search index disabled while attempting to ${action}:`, error);
  }
  searchIndexDisabledReason = message;
}

function canUseSearchIndex(): boolean {
  return !searchIndexDisabledReason;
}

function upsertIndexedNote(filename: string, content: string): void {
  if (!canUseSearchIndex()) {
    return;
  }

  try {
    searchIndex.upsertNote(filename, content);
  } catch (error) {
    disableSearchIndex(error, `index note ${filename}`);
  }
}

function removeIndexedNote(filename: string): void {
  if (!canUseSearchIndex()) {
    return;
  }

  try {
    searchIndex.removeNote(filename);
  } catch (error) {
    disableSearchIndex(error, `remove indexed note ${filename}`);
  }
}

function initializeSearchIndex(): SearchIndexStatus {
  if (!canUseSearchIndex()) {
    return getSearchIndexStatusSafe();
  }

  try {
    return searchIndex.initialize(settings.notesFolder, getIndexedNotes());
  } catch (error) {
    disableSearchIndex(error, 'initialize search index');
    return getSearchIndexStatusSafe();
  }
}

function rebuildSearchIndex(): SearchIndexStatus {
  if (!canUseSearchIndex()) {
    return getSearchIndexStatusSafe();
  }

  try {
    return searchIndex.rebuild(settings.notesFolder, getIndexedNotes());
  } catch (error) {
    disableSearchIndex(error, 'rebuild search index');
    return getSearchIndexStatusSafe();
  }
}

function clearSearchIndex(): SearchIndexStatus {
  if (!canUseSearchIndex()) {
    return getSearchIndexStatusSafe();
  }

  try {
    return searchIndex.clear(settings.notesFolder);
  } catch (error) {
    disableSearchIndex(error, 'clear search index');
    return getSearchIndexStatusSafe();
  }
}

