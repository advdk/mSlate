let mainWindow: BrowserWindow | null = null;
let notesWatcher: fs.FSWatcher | null = null;
let searchSyncTimeout: ReturnType<typeof setTimeout> | null = null;

function scheduleSearchIndexSync(): void {
  if (searchSyncTimeout) clearTimeout(searchSyncTimeout);
  searchSyncTimeout = setTimeout(() => {
    searchSyncTimeout = null;
    try {
      rebuildSearchIndex();
    } catch (error) {
      console.error('Failed to synchronize search index:', error);
    }
  }, 200);
}

function startWatchingNotes(): void {
  stopWatchingNotes();
  ensureNotesFolder();
  notesWatcher = fs.watch(settings.notesFolder, { recursive: true }, (_eventType, filename) => {
    if (filename && isNotePath(filename.replace(/\\/g, '/')) && mainWindow) {
      mainWindow.webContents.send('notes-changed');
      scheduleSearchIndexSync();
    }
  });
}

function stopWatchingNotes(): void {
  if (searchSyncTimeout) {
    clearTimeout(searchSyncTimeout);
    searchSyncTimeout = null;
  }
  if (notesWatcher) {
    notesWatcher.close();
    notesWatcher = null;
  }
}

function getEventWindow(event: IpcMainInvokeEvent): BrowserWindow {
  const window = BrowserWindow.fromWebContents(event.sender);
  if (!window) {
    throw new Error('No window available');
  }

  return window;
}

ipcMain.handle('export-pdf', async (_event, filename: string) => {
  const normalizedFilename = normalizeNotePath(filename);
  if (!isNotePath(normalizedFilename)) throw new Error('Invalid note filename');
  if (!mainWindow) throw new Error('No window available');

  const exportName = path.posix.basename(normalizedFilename, '.md');
  const result = await dialog.showSaveDialog(mainWindow, {
    defaultPath: `mSlate-${exportName}.pdf`,
    filters: [{ name: 'PDF', extensions: ['pdf'] }],
  });
  if (result.canceled || !result.filePath) return false;

  const pdfData = await mainWindow.webContents.printToPDF({
    printBackground: true,
    margins: { top: 0.5, bottom: 0.5, left: 0.5, right: 0.5 },
  });
  fs.writeFileSync(result.filePath, pdfData);
  return true;
});

